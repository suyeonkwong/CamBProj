package kr.or.ddit.student.dorm.vo;

public class DormVO {
	
	private String stdId;                 //학번
	private String year;                  //년도
	private String acptYn;                //합격 여부
	private String feePayYn;              //비용 납부 여부
	private String semCode;               //학기 코드
	private String entrApplyNum;          //입실 신청 번호 - 기본키
	private String buildCode;             //건물 코드
	
	private String roomIdnNum;			  //생활관 방 식별번호
	
	private String name;				  //이름
	private String gen;					  //이름
	private String korNameUniv;           //단과대학이름
	private String korNameDept;           //학과이름
	private String phNum;                 //핸드폰
	private String email;                 //이메일
	private String zipcode;               //우편번호
	private String addr;                  //주소
	private String addrDetail;            //상세주소
	
	public String getStdId() {
		return stdId;
	}
	public void setStdId(String stdId) {
		this.stdId = stdId;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getAcptYn() {
		return acptYn;
	}
	public void setAcptYn(String acptYn) {
		this.acptYn = acptYn;
	}
	public String getFeePayYn() {
		return feePayYn;
	}
	public void setFeePayYn(String feePayYn) {
		this.feePayYn = feePayYn;
	}
	public String getSemCode() {
		return semCode;
	}
	public void setSemCode(String semCode) {
		this.semCode = semCode;
	}
	public String getEntrApplyNum() {
		return entrApplyNum;
	}
	public void setEntrApplyNum(String entrApplyNum) {
		this.entrApplyNum = entrApplyNum;
	}
	public String getBuildCode() {
		return buildCode;
	}
	public void setBuildCode(String buildCode) {
		this.buildCode = buildCode;
	}
	public String getRoomIdnNum() {
		return roomIdnNum;
	}
	public void setRoomIdnNum(String roomIdnNum) {
		this.roomIdnNum = roomIdnNum;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGen() {
		return gen;
	}
	public void setGen(String gen) {
		this.gen = gen;
	}
	public String getKorNameUniv() {
		return korNameUniv;
	}
	public void setKorNameUniv(String korNameUniv) {
		this.korNameUniv = korNameUniv;
	}
	public String getKorNameDept() {
		return korNameDept;
	}
	public void setKorNameDept(String korNameDept) {
		this.korNameDept = korNameDept;
	}
	public String getPhNum() {
		return phNum;
	}
	public void setPhNum(String phNum) {
		this.phNum = phNum;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getZipcode() {
		return zipcode;
	}
	public void setZipcode(String zipcode) {
		this.zipcode = zipcode;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getAddrDetail() {
		return addrDetail;
	}
	public void setAddrDetail(String addrDetail) {
		this.addrDetail = addrDetail;
	}
	@Override
	public String toString() {
		return "DormVO [stdId=" + stdId + ", year=" + year + ", acptYn=" + acptYn + ", feePayYn=" + feePayYn
				+ ", semCode=" + semCode + ", entrApplyNum=" + entrApplyNum + ", buildCode=" + buildCode
				+ ", roomIdnNum=" + roomIdnNum + ", name=" + name + ", gen=" + gen + ", korNameUniv=" + korNameUniv
				+ ", korNameDept=" + korNameDept + ", phNum=" + phNum + ", email=" + email + ", zipcode=" + zipcode
				+ ", addr=" + addr + ", addrDetail=" + addrDetail + "]";
	}

}

package kr.or.ddit.student.registrationLecture.vo;

public class LectureOpenFormVO {
	private LectureOpenVO searchLectureOpenVO; // 검색 용
	private LectureOpenVO lectureOpenVO; // 데이터 용
	
	public LectureOpenFormVO() {
		this.searchLectureOpenVO = new LectureOpenVO();
		this.lectureOpenVO = new LectureOpenVO();
	}

	public LectureOpenVO getSearchLectureOpenVO() {
		return searchLectureOpenVO;
	}

	public void setSearchLectureOpenVO(LectureOpenVO searchLectureOpenVO) {
		this.searchLectureOpenVO = searchLectureOpenVO;
	}

	public LectureOpenVO getLectureOpenVO() {
		return lectureOpenVO;
	}

	public void setLectureOpenVO(LectureOpenVO lectureOpenVO) {
		this.lectureOpenVO = lectureOpenVO;
	}
	
	
}

package kr.or.ddit.student.readingRoom.vo;

public class ReadingRoomSeatVO {
	
	private String roomIdnNum;           //열람실 식별 번호
	private String seatIdnNum;           //좌석 식별 번호
	private String avlYn;                //사용 가능 여부
	private String seatNum;              //좌석 번호
	
	private int useSeat;				 //사용석

	public String getRoomIdnNum() {
		return roomIdnNum;
	}

	public void setRoomIdnNum(String roomIdnNum) {
		this.roomIdnNum = roomIdnNum;
	}

	public String getSeatIdnNum() {
		return seatIdnNum;
	}

	public void setSeatIdnNum(String seatIdnNum) {
		this.seatIdnNum = seatIdnNum;
	}

	public String getAvlYn() {
		return avlYn;
	}

	public void setAvlYn(String avlYn) {
		this.avlYn = avlYn;
	}

	public String getSeatNum() {
		return seatNum;
	}

	public void setSeatNum(String seatNum) {
		this.seatNum = seatNum;
	}

	public int getUseSeat() {
		return useSeat;
	}

	public void setUseSeat(int useSeat) {
		this.useSeat = useSeat;
	}

	@Override
	public String toString() {
		return "ReadingRoomSeatVO [roomIdnNum=" + roomIdnNum + ", seatIdnNum=" + seatIdnNum + ", avlYn=" + avlYn
				+ ", seatNum=" + seatNum + ", useSeat=" + useSeat + "]";
	}
	
}

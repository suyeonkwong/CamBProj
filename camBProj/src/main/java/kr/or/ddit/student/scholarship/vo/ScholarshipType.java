package kr.or.ddit.student.scholarship.vo;

import kr.or.ddit.util.BaseVO.BaseVO;

public class ScholarshipType extends BaseVO {
	
	private String scholaTypeCode;
	private String scholaName;
	private String minCred;
	private int rankPer;
	private int tuitReducPer;
	
	public String getScholaTypeCode() {
		return scholaTypeCode;
	}
	public void setScholaTypeCode(String scholaTypeCode) {
		this.scholaTypeCode = scholaTypeCode;
	}
	public String getScholaName() {
		return scholaName;
	}
	public void setScholaName(String scholaName) {
		this.scholaName = scholaName;
	}
	public String getMinCred() {
		return minCred;
	}
	public void setMinCred(String minCred) {
		this.minCred = minCred;
	}
	public int getRankPer() {
		return rankPer;
	}
	public void setRankPer(int rankPer) {
		this.rankPer = rankPer;
	}
	public int getTuitReducPer() {
		return tuitReducPer;
	}
	public void setTuitReducPer(int tuitReducPer) {
		this.tuitReducPer = tuitReducPer;
	}
	
	
	
}

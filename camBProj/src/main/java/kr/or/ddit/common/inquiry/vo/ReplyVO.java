package kr.or.ddit.common.inquiry.vo;

import java.util.Date;

import kr.or.ddit.util.BaseVO.BaseVO;

public class ReplyVO extends BaseVO  {

	private String memId;
	private String artcNum;
	private String oriNum;
	private String replyComment;
	private Date writeDate;
	private int reCnt;
	//조인
	private String name;
	
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public String getArtcNum() {
		return artcNum;
	}
	public void setArtcNum(String artcNum) {
		this.artcNum = artcNum;
	}
	public String getOriNum() {
		return oriNum;
	}
	public void setOriNum(String oriNum) {
		this.oriNum = oriNum;
	}
	public String getReplyComment() {
		return replyComment;
	}
	public void setReplyComment(String replyComment) {
		this.replyComment = replyComment;
	}
	public Date getWriteDate() {
		return writeDate;
	}
	public void setWriteDate(Date writeDate) {
		this.writeDate = writeDate;
	}
	public int getReCnt() {
		return reCnt;
	}
	public void setReCnt(int reCnt) {
		this.reCnt = reCnt;
	}	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	@Override
	public String toString() {
		return "ReplyVO [memId=" + memId + ", artcNum=" + artcNum + ", oriNum=" + oriNum + ", replyComment="
				+ replyComment + ", writeDate=" + writeDate + ", reCnt=" + reCnt + ", name=" + name + "]";
	}

	
	
}

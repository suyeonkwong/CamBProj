package kr.or.ddit.admin.member.vo;

import java.util.Date;

public class StudentVO extends MemberVO{
	
	private String stdId;			// 학번
	private String univDeptNum;		// 학과 번호
	private Date admDate;			// 입학 일자
	private Date grdDate;			// 졸업 일자
	private String rgstSem;			// 현재 등록 학기
	private String acadStatCode;	// 학사 상태 코드
	private String advProf;			// 지도 교수
	
	
	public String getStdId() {
		return stdId;
	}
	public void setStdId(String stdId) {
		this.stdId = stdId;
	}
	public String getUnivDeptNum() {
		return univDeptNum;
	}
	public void setUnivDeptNum(String univDeptNum) {
		this.univDeptNum = univDeptNum;
	}
	public Date getAdmDate() {
		return admDate;
	}
	public void setAdmDate(Date admDate) {
		this.admDate = admDate;
	}
	public Date getGrdDate() {
		return grdDate;
	}
	public void setGrdDate(Date grdDate) {
		this.grdDate = grdDate;
	}
	public String getRgstSem() {
		return rgstSem;
	}
	public void setRgstSem(String rgstSem) {
		this.rgstSem = rgstSem;
	}
	public String getAcadStatCode() {
		return acadStatCode;
	}
	public void setAcadStatCode(String acadStatCode) {
		this.acadStatCode = acadStatCode;
	}
	public String getAdvProf() {
		return advProf;
	}
	public void setAdvProf(String advProf) {
		this.advProf = advProf;
	}
	
	

}
